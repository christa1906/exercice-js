// 1. Demander deux nombres
let a = prompt("Entrez le premier nombre :");
let b = prompt("Entrez le deuxième nombre :");

// 2. Convertir en Number()
a = Number(a);
b = Number(b);

// 3. Calculer les opérations
let somme = a + b;
let difference = a - b;
let produit = a * b;
let division = a / b;

// 4. Afficher les résultats dans la console
console.log("Somme :", somme);
console.log("Différence :", difference);
console.log("Produit :", produit);
console.log("Division :", division);

